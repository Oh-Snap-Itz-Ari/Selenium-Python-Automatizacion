# 1. Creamos una variable que contenga un nombre y una cadena
nombre = "aLeX"
cadena = "HTML, CSS, LARAVEL, ANGULAR"

# 2. Imprimimos de diferentes maneras
print("\nImpresión normal: "+nombre)
print("Impresión mayúscula: "+nombre.upper())
print("Impresión minúscula: "+nombre.lower())
print("Impresión tipo oración (Primera mayúscula): "+nombre.capitalize())

print("\nImpresión de la cadena (normal): "+cadena)
print("\nImpresión de la cadena separada por comas y como tipo Array: ")
print(cadena.split(','))

