# 1. Se crea la variable texto, que contenga una pequeña presentación
texto = "Hola, soy Alex Fabián Melo Gómez"

# 2. Se imprime la cadena completa
print("\nSe imprime la cadena completa:\n"+texto)

# 3. Se imprime el caracter número 10, que en este caso seria la A de Alex (Comienza a contar desde 0)
print("\nSe imprime el caracter número 10, que en este caso seria la A de Alex (Comienza a contar desde 0):\n"+texto[10])

# 4. Se imprime del caracter número 10 al 14, que en este caso seria Alex (Comienza a contar desde 0)
print("\nSe imprime del caracter número 10 al 14, que en este caso seria Alex (Comienza a contar desde 0):\n"+texto[10:14])

# 5. Se imprime a partir del caracter número 10 (Comienza a contar desde 0)
print("\nSe imprime a partir del caracter número 10 (Comienza a contar desde 0):\n"+texto[10:])

# 6. Se imprime a partir del ultimo caracter 5 espacios hacia la izquierda (en este caso seria Gómez)
print("\nSe imprime a partir del ultimo caracter 5 espacios hacia la izquierda (en este caso seria Gómez):\n"+texto[-5:])